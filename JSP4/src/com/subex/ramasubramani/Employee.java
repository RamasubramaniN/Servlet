package com.subex.ramasubramani;

public class Employee extends Person
{
	int empId;
	String position;

	public int getEmpId()
	{
		return empId;
	}

	public void setEmpId( int empId )
	{
		this.empId = empId;
	}

	public String getPosition()
	{
		return position;
	}

	public void setPosition( String position )
	{
		this.position = position;
	}

}
