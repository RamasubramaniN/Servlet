package com.subex.ramasubramani;

public class Person
{
	private int age;
	private String fullname;

	public int getAge()
	{
		return age;
	}

	public void setAge( int age )
	{
		this.age = age;
	}

	public String getFullname()
	{
		return fullname;
	}

	public void setFullname( String fullname )
	{
		this.fullname = fullname;
	}

}
